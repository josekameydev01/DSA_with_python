# Initialize my array.
# Time complexity: O(1)
my_array = [1,3,5]

# Reading all values using a for loop.
# Time complexity: O(N)
for i in range(len(my_array)):
    print(my_array[i])

# Reading all values using a while loop.
# Time complexity: O(N)
i = 0
while i < len(my_array):
    print(my_array[i])
    i += 1